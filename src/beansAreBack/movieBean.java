package beansAreBack;

public class movieBean {
	private String movieName, movieYear, movieDirector, movieStars, movieAwards ;

	public String getMovieName() {
		return movieName;
	}

	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}

	public String getMovieYear() {
		return movieYear;
	}

	public void setMovieYear(String movieYear) {
		this.movieYear = movieYear;
	}

	public String getMovieDirector() {
		return movieDirector;
	}

	public void setMovieDirector(String movieDirector) {
		this.movieDirector = movieDirector;
	}

	public String getMovieStars() {
		return movieStars;
	}

	public void setMovieStars(String movieStars) {
		this.movieStars = movieStars;
	}

	public String getMovieAwards() {
		return movieAwards;
	}

	public void setMovieAwards(String movieAwards) {
		this.movieAwards = movieAwards;
	}
	
}
